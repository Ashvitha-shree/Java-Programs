class Enhancedfor{
    public static void main(String[] args) {
        int[] arr={22,33,44,55};
        String arr1[]={"one","two","three"};
        for(int var:arr){
            System.out.println(var);
        }
        for(String var:arr1){
            System.out.println(var);
        }
    }
} 